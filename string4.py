a = 'có gì hótcbc'

# capitalize() viết hoa chữ cái đầu tiên, tất cả các chữ còn lại viết thường
# upper() tất cả viết hoa
# lower() tất cả viết thường
# swapcase() hoán đổi chữ viết hoa thành viết thường và ngược lại
# title() viết hoa chữ cái đầu tiên các từ
# center(width,[fillchar]) trả về chuỗi căn giữa có độ rộng width, fillchar là chuỗi có độ dài là 1
# rjust(w,[f]) căn phải
# ljust(w,[f]) căn trái
# encode(encoding = "utf-8", errors = 'strict')
# join(['1','2','3']) cộng danh sách các chuỗi tuần tự
# replace(chuỗi 1, chuỗi 2, count) thay thế một chuỗi bằng chuỗi khác theo số lần count
# strip bỏ ký tự đầu và ký tự cuối
# rstrip() right strip; lstrip() left strip
b = a.strip('cb')
print(b)